
package org.joget.marketplace.model;

public class FieldProperties {

    private String encryption;
    private String readonly;
    private String style;
    private String label;
    private String readonlyLabel;
    private String storeNumeric;
    private String id;
    private String value;
    private String maxlength;
    private Validator validator;
    private String placeholder;
    private String size;
    private String workflowVariable;

    public String getEncryption() {
        return encryption;
    }

    public void setEncryption(String encryption) {
        this.encryption = encryption;
    }

    public String getReadonly() {
        return readonly;
    }

    public void setReadonly(String readonly) {
        this.readonly = readonly;
    }

    public String getStyle() {
        return style;
    }

    public void setStyle(String style) {
        this.style = style;
    }

    public String getLabel() {
        return label;
    }

    public void setLabel(String label) {
        this.label = label;
    }

    public String getReadonlyLabel() {
        return readonlyLabel;
    }

    public void setReadonlyLabel(String readonlyLabel) {
        this.readonlyLabel = readonlyLabel;
    }

    public String getStoreNumeric() {
        return storeNumeric;
    }

    public void setStoreNumeric(String storeNumeric) {
        this.storeNumeric = storeNumeric;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }

    public String getMaxlength() {
        return maxlength;
    }

    public void setMaxlength(String maxlength) {
        this.maxlength = maxlength;
    }

    public Validator getValidator() {
        return validator;
    }

    public void setValidator(Validator validator) {
        this.validator = validator;
    }

    public String getPlaceholder() {
        return placeholder;
    }

    public void setPlaceholder(String placeholder) {
        this.placeholder = placeholder;
    }

    public String getSize() {
        return size;
    }

    public void setSize(String size) {
        this.size = size;
    }

    public String getWorkflowVariable() {
        return workflowVariable;
    }

    public void setWorkflowVariable(String workflowVariable) {
        this.workflowVariable = workflowVariable;
    }
}
